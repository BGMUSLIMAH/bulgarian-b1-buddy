// Подготовка за изпит по български език за гражданство — ЦКОКУО
// 8 пълни теста × 20 въпроса | Успех = 12/20 | 1 час
// Структура: Q1-5 четене с разбиране, Q6-8 граматика,
// Q9-11 синоними, Q12-14 антоними, Q15-16 фразеологизми,
// Q17-19 правопис, Q20 пунктуация

export interface CitizenshipQuestion {
  id: string;
  prompt: string;
  options: string[];
  correct: number;
  explanation?: string;
}

export interface CitizenshipExam {
  id: string;
  title: string;
  passage: string;
  passageEn?: string;
  questions: CitizenshipQuestion[];
}

export function shuffleExamQuestions(exam: CitizenshipExam): CitizenshipExam {
  const shuffled = [...exam.questions];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return { ...exam, questions: shuffled };
}

export function shuffleAnswerOptions(question: CitizenshipQuestion): CitizenshipQuestion {
  const indexed = question.options.map((opt, i) => ({ opt, isCorrect: i === question.correct }));
  for (let i = indexed.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [indexed[i], indexed[j]] = [indexed[j], indexed[i]];
  }
  return {
    ...question,
    options: indexed.map(x => x.opt),
    correct: indexed.findIndex(x => x.isCorrect),
  };
}

export function shuffleAllOptions(exam: CitizenshipExam): CitizenshipExam {
  return {
    ...exam,
    questions: exam.questions.map(shuffleAnswerOptions),
  };
}

