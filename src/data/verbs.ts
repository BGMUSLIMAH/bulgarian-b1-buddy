// ---------------- Past + Future tense for top high-frequency verbs ----------------
// Past = минало свършено (aorist), full conjugation (аз/ти/той/ние/вие/те).
// Future = ще + present, full conjugation (ще + present forms; "се" stays before/after the verb depending on the verb).
// Hand-curated for the most useful 30+ verbs. Stored in a separate map keyed by
// the infinitive ("inf" column) so the rest of the verb data stays untouched.
export interface VerbTenseSet {
  az: string;
  ti: string;
  toj: string;
  nie: string;
  vie: string;
  te: string;
}
export interface VerbTense {
  past: VerbTenseSet;
  future: VerbTenseSet;
  /** @deprecated kept for backwards compatibility with quiz code */
  past1sg: string;
  /** @deprecated kept for backwards compatibility with quiz code */
  future1sg: string;
}

// Helper: build a VerbTense from past + future full tables.
function tense(past: VerbTenseSet, future: VerbTenseSet): VerbTense {
  return { past, future, past1sg: past.az, future1sg: future.az };
}

